package com.yitianyike.calendar.pullserver.model.response;


public class Recommend {
	private String package_name;
	private int package_order_no;
	private String icon_url;
	private int package_id;

	public String getPackage_name() {
		return package_name;
	}

	public void setPackage_name(String package_name) {
		this.package_name = package_name;
	}

	public int getPackage_order_no() {
		return package_order_no;
	}

	public void setPackage_order_no(int package_order_no) {
		this.package_order_no = package_order_no;
	}

	public String getIcon_url() {
		return icon_url;
	}

	public void setIcon_url(String icon_url) {
		this.icon_url = icon_url;
	}

	public int getPackage_id() {
		return package_id;
	}

	public void setPackage_id(int package_id) {
		this.package_id = package_id;
	}

}
