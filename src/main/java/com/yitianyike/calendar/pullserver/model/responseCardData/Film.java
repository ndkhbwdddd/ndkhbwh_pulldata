package com.yitianyike.calendar.pullserver.model.responseCardData;

public class Film {

	private String director;
	private String icon_address;
	private String link_url;
	private String star;
	private String tv_title;
	private String type;
	private String play_date;
	private String story_brief;
	private String banner;

	public String getDirector() {
		return director;
	}

	public void setDirector(String director) {
		this.director = director;
	}

	public String getIcon_address() {
		return icon_address;
	}

	public void setIcon_address(String icon_address) {
		this.icon_address = icon_address;
	}

	public String getLink_url() {
		return link_url;
	}

	public void setLink_url(String link_url) {
		this.link_url = link_url;
	}

	public String getStar() {
		return star;
	}

	public void setStar(String star) {
		this.star = star;
	}

	public String getTv_title() {
		return tv_title;
	}

	public void setTv_title(String tv_title) {
		this.tv_title = tv_title;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getPlay_date() {
		return play_date;
	}

	public void setPlay_date(String play_date) {
		this.play_date = play_date;
	}

	public String getStory_brief() {
		return story_brief;
	}

	public void setStory_brief(String story_brief) {
		this.story_brief = story_brief;
	}

	public String getBanner() {
		return banner;
	}

	public void setBanner(String banner) {
		this.banner = banner;
	}

}
