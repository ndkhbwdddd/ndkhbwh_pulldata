package com.yitianyike.calendar.pullserver.model.responseCardData;

public class ControlDrive {

	private String date;
	private String cityname;
	private String week;
	private String xxweihao;
	private String control_drive_id;
	private String skip_url;

	public String getSkip_url() {
		return skip_url;
	}

	public void setSkip_url(String skip_url) {
		this.skip_url = skip_url;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getCityname() {
		return cityname;
	}

	public void setCityname(String cityname) {
		this.cityname = cityname;
	}

	public String getWeek() {
		return week;
	}

	public void setWeek(String week) {
		this.week = week;
	}

	public String getXxweihao() {
		return xxweihao;
	}

	public void setXxweihao(String xxweihao) {
		this.xxweihao = xxweihao;
	}

	public String getControl_drive_id() {
		return control_drive_id;
	}

	public void setControl_drive_id(String control_drive_id) {
		this.control_drive_id = control_drive_id;
	}

}
